package day1;

public class Movie {
	private String title, genre;
	private int date;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public int getDate() {
		return date;
	}
	public void setDate(int date) {
		this.date = date;
	} 
}
